def red(text):
    return "\033[91m{}\033[0m".format(text)

def green(text):
    return "\033[92m{}\033[0m".format(text)

def yellow(text):
    return "\033[93m{}\033[0m".format(text)

def blue(text):
    return "\033[94m{}\033[0m".format(text)

def purple(text):
    return "\033[95m{}\033[0m".format(text)

def cyan(text):
    return "\033[96m{}\033[0m".format(text)
